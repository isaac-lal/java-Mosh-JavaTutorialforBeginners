public class my_first_program {
    public static void main(String[] args) {
        System.out.println("I love pizza\n"); // println prints text on its own line (\n does same thing = 2 lines)
                                               // if print ln isn't used, all the text will print on one line
        System.out.println("It's really good!");
        System.out.println("\tYum!"); // \t gives a tab between where its placed
        System.out.println("\"Yessir"); // displays quotes (needs backslash). To display backslash, use double backslash

        // This is a comment
        /*
         * this
         * is
         * a
         * comment
         */
    }
}
